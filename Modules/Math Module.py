import math

print(x = math.sqrt(16)) #square root of 16
print(x = math.pow(2,3)) #power, 2^3
print(x = math.ceil(4.3)) #rounds up (x=5)
print(x = math.floor(7.8)) #rounds down (x=7)
print(x = math.log(34)) #log(e)34 (x=3.52)
print(x = math.log10(100)) #log(10)100, x=2
print(x = math.sin(3.14)) #angle in radius
print(x = math.cos(math.pi)) 
print(x = math.tan(math.pi * 2)) 
print(x = math.asin(-1)) #inverse sine
print(x = math.sinh(1)) #hyperbolic sine
print(x = math.pi) #math pi 
print(x = math.fabs(-5.1)) #print the ultimate value of -5.1
print(x = math.exp(1.0)) #returns exponential of x:e^x
print(x = math.fmod(9.8, 4.0)) #returns the module remainder (modulo of x/y)